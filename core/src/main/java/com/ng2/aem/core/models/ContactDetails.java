package com.ng2.aem.core.models;

public class ContactDetails {

	private String title;
	private String description;
	private String firstNameLabel;
	private String lastNameLabel;
	private String emailLabel;
	private String addressLabel;
	private String contactJson;
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getFirstNameLabel() {
		return firstNameLabel;
	}
	public void setFirstNameLabel(String firstNameLabel) {
		this.firstNameLabel = firstNameLabel;
	}
	public String getLastNameLabel() {
		return lastNameLabel;
	}
	public void setLastNameLabel(String lastNameLabel) {
		this.lastNameLabel = lastNameLabel;
	}
	public String getEmailLabel() {
		return emailLabel;
	}
	public void setEmailLabel(String emailLabel) {
		this.emailLabel = emailLabel;
	}
	public String getAddressLabel() {
		return addressLabel;
	}
	public void setAddressLabel(String addressLabel) {
		this.addressLabel = addressLabel;
	}
	public String getContactJson() {
		return contactJson;
	}
	public void setContactJson(String contactJson) {
		this.contactJson = contactJson;
	}

}
